			<section id="intro">
				<h4>Introduction</h4>
			
				<p>Hack This Site is a free training ground for users to test and expand their hacking skills. Our community is dedicated to facilitating an open learning environment by providing a series of hacking challenges, articles, resources, and discussion of the latest happenings in hacker culture. We are an online movement of artists, activists, hackers and anarchists who are organizing to create new worlds.</p>
				<p>This guide is an introduction to the site, community, philosophy and project organization. This project is entirely what you make of it. We encourage people to take an active role in the development of this community and to copy and redistribute this guide on their own.</p>
			</section>
			
			<section id="philosophy">
				<h4>Hacking Philosophy</h4>
				<p>We believe everyone should have free access to all information. Hacking should not be a privileged skill - everyone should have the opportunity to learn about computer security in the age of technology. We seek to facilitate a free and open training ground where people can test and expand their skills in a legal and realistic environment.</p>
			</section>
			
			<section id="ethics">
				<h4>Ethics</h4>
				<p>Considering that several of the hacking challenges are simulated web defacements, the question of the ethics of hacking is repeatedly brought up. We like to consider hacking itself to be a tool, a skill which in itself is neutral, a means without end. It can be used for good (for the benefit of all) or bad (mindless destruction or theft). We do not encourage negative use of the information we provide. We are more concerned with the greater risks of not distributing this information and are ready to accept the consequences.</p>
			</section>
			
			<section id="community">
				<h4>Community</h4>
				<p>Unlike many wargame and security sites, HackThisSite is a community where all users can contribute and participate in the development of the site. We are an open society that encourages active participation, and we have set up a number of ways people can become involved. In addition, we have a very active IRC network, forums, and an open publishing system where people can submit articles and external resources to the site. We are a friendly atmosphere where we help people who ask intelligent questions. Join us and participate in very lively and passionate debates about technology and worldly events. There is no social hierarchy; we are all equal. HTS is what you make of it!</p>
				<p>We wish to promote the use of hacking as a tool to fight for social justice causes. Hacking is justified if it is used to combat oppression, censorship or inequality. While we do not encourage or participate in illegal activities, we do want to present hacktivism as a positive alternative to mindless black hat hacking. Gain the skills and join the struggle for freedom!</p>
			</section>
			
			<section id="challenges">
				<h4>Hacking Challenges</h4>
				<p>At the heart of the project lies the hacking challenges. We have dozens of simulated wargames that allow users to put their skills to the test in a legal, realistic environment. There are several kinds of challenges each demonstrating different types of skills.</p>
			</section>
			
			<section id="project_org">
				<h4>Project Organization</h4>
				<p>If you are interested in becoming part of HTS, the first step is not to submit an application or directly ask us. Try contributing to the project through a number of other ways - becoming active on IRC, writing articles, giving lectures, contributing ideas, etc. Have something to offer to HTS, then declare your intention about being on staff. Remember that you have to win the support of existing staff members and be voted in.</p>
			</section>
			
			<section id="get_involved">
				<h4>Get Involved!</h4>
				<p>Hack This Site is not something that just happens. It is a continuously evolving project with many active users working on many active projects. It's more than just a website, but a movement made up of people just like you.</p>
			</section>
			
