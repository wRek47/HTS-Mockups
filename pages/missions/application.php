<h3>Application Challenges</h3>

<p>
	Welcome to Hack This Site application challenges!
	In this variety of hacking challenges, you are given an application with an objective and you are expected to manipulate the operating system, the executable, or through other means to complete the level.
	Each challenge operates in a different way and will require different methods to go about completing the level.
</p>

<p>
	Upon completion, the challenge will give you a password for you to enter on the website.
	This will update your HTS account, demonstrating that you have actually completed the level.
</p>

<p>
	All application challenges can be beaten without brute-forcing.
	We encourage you to try what you like on the programs but please do NOT brute-force the website.
</p>

<p>
	These levels are available for Windows(9X/ME/NT/2000/XP) and Macintosh (Mac OS X, Mac OS classic with carbon) systems only.
	If anyone is interested in developing *nix versions of these application challenges, please contact us
	<em>- basic socket programming experience is necessary.</em>
</p>

